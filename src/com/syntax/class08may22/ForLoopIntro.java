package com.syntax.class08may22;

public class ForLoopIntro {

	public static void main(String[] args) {
		
//I want to say Good Morning 5 times.
		
for(int i=1;i<5; i++) {
	System.out.println("Good Morning");//
	}
System.out.println("----------------------");
for(int i=1; i>=5; i++) {
	System.out.println("How Are you");//0
}

System.out.println("-------------------------------");
	for (int i=1;i<=5; i--)	{
		System.out.println("Hello");//infinite loop
		
		System.out.println("----------------------");
		
	}
		
	}

}
